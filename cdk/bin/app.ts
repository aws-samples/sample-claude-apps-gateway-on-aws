#!/usr/bin/env node
import 'source-map-support/register';
import * as cdk from 'aws-cdk-lib';
import { NetworkStack } from '../lib/network-stack';
import { DatabaseStack } from '../lib/database-stack';
import { SecretsStack } from '../lib/secrets-stack';
import { BuildMachineStack } from '../lib/build-machine-stack';
import { GatewayStack } from '../lib/gateway-stack';
import { AdminConsoleStack } from '../lib/admin-console-stack';
import { VpnStack } from '../lib/vpn-stack';

const app = new cdk.App();

// This reference architecture is a self-contained deployment: one VPC, one
// Aurora cluster, and two ECS Express Mode services (the gateway and its
// admin console). All five stacks below deploy into the same account and
// region, so they're wired together with direct construct references
// rather than cross-stack SSM parameters or exports -- simpler to read and
// to tear down.
const env = {
  account: process.env.CDK_DEFAULT_ACCOUNT,
  region: process.env.CDK_DEFAULT_REGION,
};

// Required application configuration, supplied via CDK context (cdk.json,
// -c flags, or environment variables prefixed per docs/02-deploy.md). None
// of this is generated by CDK -- it all comes from your Okta app
// registration (docs/01-prerequisites.md).
const oidcIssuer = app.node.tryGetContext('oidcIssuer');
const oidcClientId = app.node.tryGetContext('oidcClientId');
const adminOktaGroupName = app.node.tryGetContext('adminOktaGroupName') ?? 'claude-gateway-admins';

if (!oidcIssuer || !oidcClientId) {
  throw new Error(
    'Missing required context values. Pass them with -c, e.g.:\n' +
    '  cdk deploy --all -c oidcIssuer=https://your-org.okta.com -c oidcClientId=0oaXXXXXXXXXXXXXXXXX -c oidcClientSecret=...\n' +
    'See docs/01-prerequisites.md for how to obtain these from your Okta app registration.'
  );
}

// The OIDC client secret is real, external configuration from your Okta
// app registration -- passed the same way as oidcIssuer and oidcClientId,
// via -c. The gateway's own config schema treats client_secret as
// required and validates it at boot (confirmed against
// https://claude-code.mintlify.app/en/claude-apps-gateway-config), so this
// can't be deferred to a manual post-deploy step the way an earlier draft
// of this app did -- that caused the gateway to crash-loop on every launch
// attempt, since ECS Express Mode gives up and CloudFormation rolls back
// the whole stack before a human ever gets the chance to intervene. The
// real value must exist before ClaudeGatewayStack ever creates the ECS
// service. This is a reference/PoC-oriented deployment; production use
// should consider sourcing this from an existing out-of-band secret rather
// than CDK context, since context values are written to cdk.context.json
// on disk.
const oidcClientSecretRaw = app.node.tryGetContext('oidcClientSecret');
if (!oidcClientSecretRaw) {
  throw new Error(
    'Missing required context value. Pass it with -c, e.g.:\n' +
    '  cdk deploy --all -c oidcClientSecret="<your Okta client secret>" -c oidcIssuer=... -c oidcClientId=...\n' +
    'See docs/01-prerequisites.md and docs/02-deploy.md.'
  );
}
const oidcClientSecretValue = cdk.SecretValue.unsafePlainText(oidcClientSecretRaw);

const network = new NetworkStack(app, 'ClaudeGatewayNetworkStack', { env });

const database = new DatabaseStack(app, 'ClaudeGatewayDatabaseStack', {
  env,
  vpc: network.vpc,
  databaseSecurityGroup: network.databaseSecurityGroup,
});

const secrets = new SecretsStack(app, 'ClaudeGatewaySecretsStack', {
  env,
  postgresUrl: database.postgresUrlSecret,
  oidcClientSecretValue,
});

// Builds and pushes both container images on a real, temporary Linux
// x86_64 EC2 instance rather than locally via DockerImageAsset -- see
// build-machine-stack.ts for why this is required, not optional, given
// the gateway's own Dockerfile. This instance is torn down automatically
// once the images are pushed -- it is a SEPARATE, one-time-use resource
// from the Client VPN endpoint below, which exists for ongoing developer
// connectivity to the private gateway, not for building images.
const buildMachine = new BuildMachineStack(app, 'ClaudeGatewayBuildMachineStack', {
  env,
  vpc: network.vpc,
});

const gateway = new GatewayStack(app, 'ClaudeGatewayStack', {
  env,
  vpc: network.vpc,
  gatewayTaskSecurityGroup: network.gatewayTaskSecurityGroup,
  privateSubnets: network.privateSubnets,
  oidcIssuer,
  oidcClientId,
  adminOktaGroupName,
  secrets: secrets.gatewaySecrets,
  gatewayImageUri: buildMachine.gatewayImageUri,
});

new AdminConsoleStack(app, 'ClaudeGatewayAdminConsoleStack', {
  env,
  vpc: network.vpc,
  adminConsoleSecurityGroup: network.adminConsoleSecurityGroup,
  publicSubnets: network.publicSubnets,
  gatewayServiceArn: gateway.serviceArn,
  gatewayEndpoint: gateway.endpoint,
  gatewayTaskRoleArn: gateway.taskRoleArn,
  gatewayExecutionRoleArn: gateway.executionRoleArn,
  gatewayTaskDefinitionFamily: gateway.taskDefinitionFamily,
  sessionSecret: secrets.consoleSessionSecret,
  adminConsoleImageUri: buildMachine.adminConsoleImageUri,
});

// A self-contained AWS Client VPN endpoint (mutual TLS), so a stranger
// deploying this repo has an actual way to reach the gateway's private
// endpoint with no manual AWS console steps beyond the Okta setup this
// repo already requires -- see vpn-stack.ts for the full rationale and
// docs/02-deploy.md for how to download and import the generated .ovpn
// profile into AWS's OpenVPN client.
new VpnStack(app, 'ClaudeGatewayVpnStack', {
  env,
  vpc: network.vpc,
  privateSubnets: network.privateSubnets,
});
